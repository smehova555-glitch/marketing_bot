def generate_recommendations(data: dict, segment: str) -> str:
    """
    –§–æ—Ä–º–∏—Ä—É–µ—Ç —Ç–µ–∫—Å—Ç —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–π –Ω–∞ –æ—Å–Ω–æ–≤–µ –æ—Ç–≤–µ—Ç–æ–≤ –∏ —Å–µ–≥–º–µ–Ω—Ç–∞.
    """

    recommendations = []

    # –ë–∞–∑–æ–≤—ã–µ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏ –ø–æ –æ—Ç–≤–µ—Ç–∞–º

    if data.get("strategy") == "–ù–µ—Ç":
        recommendations.append("–ü—Ä–æ–≤–µ—Å—Ç–∏ —Å—Ç—Ä–∞—Ç–µ–≥–∏—á–µ—Å–∫—É—é —Å–µ—Å—Å–∏—é –∏ –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å –≤–µ–∫—Ç–æ—Ä —Ä–æ—Å—Ç–∞.")

    if data.get("stability") in ["–ù–µ—Ç", "–ò–Ω–æ–≥–¥–∞"]:
        recommendations.append("–ù–∞—Å—Ç—Ä–æ–∏—Ç—å —Å–∏—Å—Ç–µ–º—É –ø—Ä–∏–≤–ª–µ—á–µ–Ω–∏—è –∑–∞—è–≤–æ–∫ (–∞ –Ω–µ —Ä–∞–∑–æ–≤—ã–µ –¥–µ–π—Å—Ç–≤–∏—è).")

    if data.get("geo") in ["–ù–µ—Ç", "–ï—Å—Ç—å, –Ω–æ –Ω–µ –ø—Ä–æ–¥–≤–∏–≥–∞–µ–º"]:
        recommendations.append("–ó–∞–ø—É—Å—Ç–∏—Ç—å —Å–∏—Å—Ç–µ–º–Ω–æ–µ –ø—Ä–æ–¥–≤–∏–∂–µ–Ω–∏–µ –≤ –Ø–Ω–¥–µ–∫—Å.–ö–∞—Ä—Ç–∞—Ö –∏ 2–ì–ò–°.")

    if data.get("content") == "–ù–µ—Ç":
        recommendations.append("–†–∞–∑—Ä–∞–±–æ—Ç–∞—Ç—å –∫–æ–Ω—Ç–µ–Ω—Ç-—Å—Ç—Ä–∞—Ç–µ–≥–∏—é —Å –ø—Ä–∏–≤—è–∑–∫–æ–π –∫ –≤–æ—Ä–æ–Ω–∫–µ –ø—Ä–æ–¥–∞–∂.")

    if data.get("team") == "–ù–µ—Ç":
        recommendations.append("–ü–æ–¥–∫–ª—é—á–∏—Ç—å –≤–Ω–µ—à–Ω–µ–µ –º–∞—Ä–∫–µ—Ç–∏–Ω–≥–æ–≤–æ–µ —Å–æ–ø—Ä–æ–≤–æ–∂–¥–µ–Ω–∏–µ –≤–º–µ—Å—Ç–æ —Ö–∞–æ—Ç–∏—á–Ω—ã—Ö –ø–æ–¥—Ä—è–¥—á–∏–∫–æ–≤.")

    if data.get("ads") in ["–ù–µ—Ç", "–ü—Ä–æ–±–æ–≤–∞–ª–∏"]:
        recommendations.append("–ü–µ—Ä–µ–∑–∞–ø—É—Å—Ç–∏—Ç—å —Ä–µ–∫–ª–∞–º—É –Ω–∞ –æ—Å–Ω–æ–≤–µ —Ü–∏—Ñ—Ä –∏ –∞–Ω–∞–ª–∏—Ç–∏–∫–∏, –∞ –Ω–µ –≥–∏–ø–æ—Ç–µ–∑.")

    if not recommendations:
        recommendations.append("–ü—Ä–æ–≤–µ—Å—Ç–∏ –≥–ª—É–±–æ–∫–∏–π –∞—É–¥–∏—Ç –º–∞—Ä–∫–µ—Ç–∏–Ω–≥–æ–≤–æ–π —Å–∏—Å—Ç–µ–º—ã –¥–ª—è –º–∞—Å—à—Ç–∞–±–∏—Ä–æ–≤–∞–Ω–∏—è.")


    # –°–µ–≥–º–µ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–µ –ø–æ–∑–∏—Ü–∏–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ

    if segment == "VIP":
        intro = (
            "üíé –í—ã –Ω–∞—Ö–æ–¥–∏—Ç–µ—Å—å –≤ –∑–æ–Ω–µ –º–∞—Å—à—Ç–∞–±–∏—Ä–æ–≤–∞–Ω–∏—è.\n\n"
            "–£ –≤–∞—Å –µ—Å—Ç—å —Ä–µ—Å—É—Ä—Å, –∏ –∫–ª—é—á–µ–≤–∞—è –∑–∞–¥–∞—á–∞ ‚Äî –≤—ã—Å—Ç—Ä–æ–∏—Ç—å —Å–∏—Å—Ç–µ–º–Ω—ã–π —Ä–æ—Å—Ç."
        )

        offer = (
            "\n\n–†–µ–∫–æ–º–µ–Ω–¥—É–µ–º—ã–π —Ñ–æ—Ä–º–∞—Ç —Ä–∞–±–æ—Ç—ã:\n"
            "‚Äî –ö–æ–º–ø–ª–µ–∫—Å–Ω–æ–µ —Å–æ–ø—Ä–æ–≤–æ–∂–¥–µ–Ω–∏–µ 3 –º–µ—Å—è—Ü–∞\n"
            "‚Äî –°—Ç—Ä–∞—Ç–µ–≥–∏—è + –∫–æ–º–∞–Ω–¥–∞ + –∫–æ–Ω—Ç—Ä–æ–ª—å –ø–æ–∫–∞–∑–∞—Ç–µ–ª–µ–π\n"
            "‚Äî –ú–∞—Å—à—Ç–∞–±–∏—Ä–æ–≤–∞–Ω–∏–µ –±–µ–∑ –æ–ø–µ—Ä–∞—Ü–∏–æ–Ω–Ω–æ–≥–æ —Ö–∞–æ—Å–∞"
        )

    elif segment == "WARM":
        intro = (
            "üî• –£ –≤–∞—Å –µ—Å—Ç—å –ø–æ—Ç–µ–Ω—Ü–∏–∞–ª —Ä–æ—Å—Ç–∞, –Ω–æ —Å–∏—Å—Ç–µ–º–∞ —Ä–∞–±–æ—Ç–∞–µ—Ç –Ω–µ—Å—Ç–∞–±–∏–ª—å–Ω–æ.\n\n"
            "–û—Å–Ω–æ–≤–Ω–∞—è –∑–∞–¥–∞—á–∞ ‚Äî —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞—Ç—å –º–∞—Ä–∫–µ—Ç–∏–Ω–≥."
        )

        offer = (
            "\n\n–†–µ–∫–æ–º–µ–Ω–¥—É–µ–º—ã–π —Ñ–æ—Ä–º–∞—Ç —Ä–∞–±–æ—Ç—ã:\n"
            "‚Äî –°—Ç—Ä–∞—Ç–µ–≥–∏—á–µ—Å–∫–∞—è —Å–µ—Å—Å–∏—è\n"
            "‚Äî –ü–ª–∞–Ω –≤–Ω–µ–¥—Ä–µ–Ω–∏—è –Ω–∞ 4‚Äì6 –Ω–µ–¥–µ–ª—å\n"
            "‚Äî –ö–æ–Ω—Ç—Ä–æ–ª—å –∫–ª—é—á–µ–≤—ã—Ö —Ç–æ—á–µ–∫ —Ä–æ—Å—Ç–∞"
        )

    else:  # COLD
        intro = (
            "‚ùÑ –°–µ–π—á–∞—Å –º–∞—Ä–∫–µ—Ç–∏–Ω–≥ —Ä–∞–±–æ—Ç–∞–µ—Ç —Ñ—Ä–∞–≥–º–µ–Ω—Ç–∞—Ä–Ω–æ.\n\n"
            "–ë–µ–∑ —Å–∏—Å—Ç–µ–º–Ω–æ–≥–æ –ø–æ–¥—Ö–æ–¥–∞ —Ä–æ—Å—Ç –±—É–¥–µ—Ç –Ω–µ—Å—Ç–∞–±–∏–ª—å–Ω—ã–º."
        )

        offer = (
            "\n\n–†–µ–∫–æ–º–µ–Ω–¥—É–µ–º—ã–π –ø–µ—Ä–≤—ã–π —à–∞–≥:\n"
            "‚Äî –ü–µ—Ä—Å–æ–Ω–∞–ª—å–Ω–∞—è –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏—è\n"
            "‚Äî –†–∞–∑–±–æ—Ä —Ç–µ–∫—É—â–µ–π —Å–∏—Ç—É–∞—Ü–∏–∏\n"
            "‚Äî –ß—ë—Ç–∫–∏–π –ø–ª–∞–Ω –¥–µ–π—Å—Ç–≤–∏–π"
        )


    # –§–∏–Ω–∞–ª—å–Ω—ã–π —Ç–µ–∫—Å—Ç
    text = intro + "\n\nüìå –†–µ–∫–æ–º–µ–Ω–¥—É–µ–º—ã–µ –¥–µ–π—Å—Ç–≤–∏—è:\n\n"

    for r in recommendations:
        text += f"‚Ä¢ {r}\n"

    text += offer

    return text